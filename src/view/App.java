    public void setUserPropreties() {
        jXLabelLogIn.setText(User.getLogin());
        if (User.getFoto() != null) {
            byte[] imagebytes = null;
            try {
                imagebytes = User.getFoto().getBytes(1, (int) User.getFoto().length());
            } catch (SQLException ex) {
                Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
            }
            BufferedImage theImage = null;
            try {
                //convert byte-Array into Buffered Image (Subclass of Image)
                theImage = ImageIO.read(new ByteArrayInputStream(imagebytes));
            } catch (IOException ex) {
                Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
            }
            ImageIcon icon = new ImageIcon(theImage);
            jLabelFoto.setIcon(icon);
        }

        jLabelName.setText(User.getName());
        jLabelLastName.setText(User.getLastName());
        jLabelFunction.setText(DataManager.getFunctionName(User.getIdFunction()));
    }

