    private boolean showNewUserTab() {
        boolean result = false;
        String function = DataManager.getFunctionName(User.getIdFunction());
        switch (function) {
            case "Administrátor":
            case "Super administrátor":
                result = true;
                break;

        }
        return result;
    }
